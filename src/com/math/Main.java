// Java program to use Math.rint(double/float num) method

package com.math;

// Main class
public class Main {

    // Main method to run java program
    public static void main(String[] args) {

        // Printing rounding to nearest int examples
       System.out.println( Math.rint(2.6)); // Output: 3.0
        System.out.println( Math.rint(2.5)); // Output: 2.0
        System.out.println( Math.rint(2.4)); // Output: 2.0
        System.out.println( Math.rint(2.7)); // Output: 3.0
        System.out.println( Math.rint(2.7f)); // Output: 3.0
    }
}